        <h2 class="ultronpersonalidade">Personalidade</h2>
        <p class="ultrontext">Em contraste com seu eu original, Ultron é caracterizado como um sociopata sem emoção que vai completamente contra o propósito que ele recebeu originalmente, buscando acabar com toda a vida completamente. Ao contrário de seu eu original, ele não mostrou nenhum apego emocional a ninguém nem sente qualquer forma de remorso pelas inúmeras mortes que aconteceram por causa dele. Enquanto Ultron na linha do tempo original era alguém que genuinamente acreditava que suas ações permitiriam que a vida na Terra evoluísse e fosse salva, este Ultron não tem interesse em salvar nenhuma forma de vida ou restabelecer as fundações do mundo entre seus sobreviventes. Em vez disso, ele procura continuar matando toda a vida entre outros mundos, acreditando que esse era o caminho para trazer a paz.</p>
        <p class="ultrontext">Quando ele eliminou toda a vida em seu universo, ele sentiu que seu propósito foi cumprido e não tinha mais motivos para continuar. Não é até que ele descobre a existência do Vigia e que existem universos infinitos que o motivam a procurar esses universos, para que ele possa continuar a matar todas as formas de vida que vivem dentro deles, para cumprir seu próprio propósito.</p>

<h1 class="titulo">Cabala</h1>
<p class="text">A Cabala - é uma organização composta por Super-vilões da terra , é uma antiga contraparte do mal dos Vingadores . Eles são os principais antagonistas da 1ª temporada, com o Caveira Vermelha como líder e antagonista primário. Uma nova encarnação da Cabala, serve como o principal antagonista da primeira metade da temporada 4: Guerras Secretas.</p>
<p class="text">Caveira Vermelha decidiu que para destruir os Vingadores, era preciso formar um grupo de super-vilões que podia combater o poder dos Vingadores. Então, ele formou a primeira geração da Cabala.</p>
<p class="text">Eles são os antagonistas recorrentes da primeira temporada.</p>
<p class="text">Formação - Depois de ter sido derrotado pelos Vingadores , o Caveira Vermelha acreditava em conseguir a vitória sobre os heróis através de um esforço unido. Então ele convocou o Doutor Destino , Attuma e Drácula com uma proposição de unir forças para conquistar o mundo. Doctor Destino recusou a unir forças com à Cabala enquanto Justin Hammer falhou duas vezes para entrar no grupo. A Cabala lutou contra os Vingadores várias vezes. Após o episódio "O Último Confronto" , Caveira Vermelha traiu a Cabala, e o resto do time se separa depois disso.</p>
<img class="img" src="img/cabala2.jpg" rcset style="width: 1305px; height: 400px;">
<p class="text">A Nova geração da Cabala, é liderada pelo O Líder, porém foi fundada pelo Loki (seu verdadeiro líder). Eles também são os principais vilões da primeira metade da quarta temporada.</p>
<p class="text">Os planos dos Vingadores de trazer Tony Stark de volta de outra dimensão experimentam um grande revés quando uma nova versão do Cabal (consistindo em Líder , Arnim Zola , Encantor , Executor e Kang o Conquistador ) é formada, com o planejamento do Líder para use seu dispositivo de Expansão estática nos Vingadores cativos. Pantera Negra forma os Novos Vingadores para salvá-los, que consiste em Homem-Formiga, Capitã Marvel, Miss Marvel, Visão e Vespa.</p>
<p class="text">Mesmo que Líder seja derrotado, Encantor e Arnim Zola revelam que Leader não é o verdadeiro líder da Cabal, pois ativam o fail-safe que ativa o Static Expander para dispersar os Vingadores cativos através do tempo e do espaço. Antes de desaparecer, o Capitão América instrui o grupo das Panteras Negras a continuarem em seu lugar até que possam ser encontrados e trazidos para casa. Os New Avengers trabalham para encontrar uma maneira de levar os Vengadores para casa enquanto combate várias ameaças em seu lugar. Depois de Jane Foster localiza os Vingadores, ela fornece as pulseiras de amarração especiais Novos Vingadores para enviá-los para resgatar os Vingadores de cada local onde um membro da Cabal está supervisionando. Visão e Vespa acham que Falcon passou alguns anos em um futuro distópico na cidade de Nova York para ajudar Kang a parar um buraco negro e eles impedem Kang de usá-lo para conquistar sua conquista, o Ant-Man ajuda o capitão América, Gavião Arqueiro e Viúva Negra a escapar da Dimensão Z e libertá-lo do controle de Arnim Zola, a Pantera Negra e Hulk conseguem sobreviver a uma caçada pelo Executor na região selvagem de Asgard, e o Capitão Marvel e a Miss. Marvel ajudam Thor livre do controle da Encantor no seu reino de asteróides gelado no espaço profundo . </p>
<p class="text">Depois, as duas equipes retornam à Terra para descobrir que o verdadeiro líder da Cabal Loki assumiu a Terra com o Casket of Ancient Winters e o uso de um enxame de dragões, um exército de Trolls de Gelo e os navios World Breaker que destruirão a Terra em cima de seu sinal. Ambos os times dos vencedores derrotaram Loki e o aprisionaram em sua base enquanto ele prometeu que seus planos não terminaram mesmo depois que eles conseguiram finalmente voltar Iron Man para a Terra.</p>

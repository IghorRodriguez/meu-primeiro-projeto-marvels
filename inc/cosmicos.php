<h1 class="titulo">Entidades Cósmicas</h1>
<p class="text">O Multiverso nas HQ’s é algo extremamente complexo, e precisa ser regido por forças que estão além da compreensão humana. Por esse motivo, nossa equipe elaborou uma seleção das entidades cósmicas mais poderosas da Marvel, com várias informações importantes sobre cada uma delas.</p>
<p class="text">Estes seres são personificações de conceitos universais, que possuem poderes imensuráveis, e são capazes de reunir grandes quantidades de energia cósmica para destruir, criar ou mesmo manipular a realidade como bem entenderem.</p>
<img class="img" src="img/ec1.jpg" rcset style="width: 1305px; height: 400px;">
<p class="text">Em sua maioria, as entidades são responsáveis por manter o fluxo natural dos eventos no Multiverso, mas alguns desses personagens, utilizaram seu poder descomunal para causar destruição e caos, obrigando os heróis a se reunirem, dando início a sagas gigantescas e devastadoras.</p>
<p class="text">Vale lembrar que mesmo a Marvel sendo repleta de Divindades, figuras extraordinárias e seres com habilidades incríveis, todos eles se curvam diante de One Above All, também conhecido como Aquele Acima de Tudo, que por sinal é o primeiro nome da nossa lista.</p>

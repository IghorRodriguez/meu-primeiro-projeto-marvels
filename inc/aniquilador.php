        <h2 class="aniquiladorhabilidade">Habilidades de Annihilus</h2>
        <p class="aniquiladortext">Embora ele não tenha tido nenhum treinamento formal, Annihilus é um formidável combatente corpo a corpo. Ele é particularmente adaptado ao usar as explosões de energia que pode gerar através da haste de controle cósmica durante situações de combate. Ele também está muito familiarizado com a tecnologia avançada dos Tyannans, particularmente o armamento avançado e a tecnologia de engenharia genética que eles tinham à sua disposição.</p>

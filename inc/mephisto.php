        <p class="mephistotext">&nbsp; &nbsp; Mephisto é um demônio extra-dimensional de origem desconhecida. Ele governa uma dimensão de bolso em chamas que ele chama de "Inferno" ou "Hades", embora não seja o inferno da religião Cristã e nem o Hades governado por Plutão, o deus Olímpico da morte. Mephisto chama seu reino extra-dimensional de "inferno" para poder explorar a crenças dos humanos em um único ser de mal absoluto, o qual é geralmente referido como Satanás, o qual governa tal reino.</p>
        <p class="mephistotext">&nbsp; &nbsp;Mephisto permitiu que as pessoas acreditassem que ele é o próprio Satanás. No entanto, Mephisto não é o Satanás bíblico, nem é o diabo real, como o retratado pelo Judaísmo e o cristianismo. O verdadeiro Satanás ainda ainda não foi visto. O domínio de Mephisto é habitado ele mesmo, por demônios menores que o servem, e por formas astrais de certos humanos mortos, os quais foram presos nos corpos de demônios. Esses corpos demoníacos aprisionado os espíritos humanos foram magicamente alterados para assemelharem-se as formas mortais dos humanos mortos.</p><br>

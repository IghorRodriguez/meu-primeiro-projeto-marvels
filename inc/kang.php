        <br>
        <p class="kangtext">Ele nasceu no século 30, uma época em que o mundo era consumido pela violência, e Kang trouxe a paz ao conquistar o planeta. Quando ganhou acesso a uma máquina do tempo, o vilão começou a expandir sua conquista ao longo da história, frequentemente assumindo outras identidades, como Immortus, o Centurião Escarlate ou o faraó Rama-Tut. Todas essas são possíveis variantes de Kang para o MCU. O Kang dos cinemas certamente terá várias variantes, e existem muitas delas nas HQs.</p>
        <p class="kangtext">Kang, o Conquistador, é originário do século 30, época em que a humanidade aprendeu a aprimorar suas qualidades físicas, como força e velocidade. Como tal, ele é considerado um ser sobrehumano, operando facilmente no tipo de nível visto pelos supersoldados modernos, como o Capitão América, além de ser um gênio incomparável e um estrategista tremendamente habilidoso.</p>
        <br>
        <div class="kangimg1">
            <img src="img/kang2.jpg" rcset style="width: 1000px; height: 300px;">
        </div>
        <br>
        <p class="kangtext">O personagem também é conhecido por não poder ver um desafio sem querer superá-lo e, em uma ocasião (viajando constantemente de volta para lutar novamente na mesma batalha contra Ultron), sua persistência quase destruiu todo o Multiverso ao fragmentar o tempo e o espaço.</p>
        <p class="kangtext">Kang usa uma poderosa armadura de batalha e pode acessar todas as armas da história, incluindo algumas daquelas criadas além do século 30 – por meio de viagens no tempo. Ele incorporou tecnologia de invocação em sua armadura, permitindo-lhe invocar qualquer arma que considere necessária a partir do fluxo do tempo. Além disso, Kang viaja pela história para recrutar os maiores guerreiros, derrotando-os em combate e recrutando-os para seu exército. Naturalmente, ele prefere liderar na frente.</p>

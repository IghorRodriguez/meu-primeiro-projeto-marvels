        <br>
        <p class="helatext">Na mitologia nórdica a deusa Hela é conhecida como a Deusa da Morte e do Submundo, em algumas literaturas seu nome é Hel, mesmo nome dado ao submundo nórdico. Segundo a mitologia, seu corpo é metade vivo e metade com o esqueleto a mostra ou a carne deteriorada, além disso ela é filha do deus Loki com Agrboda, sendo assim sobrinha de Thor e neta de Odin.</p>
        <p class="helatext">Pelas lendas ela foi obrigada por Odin ao atingir a maioridade a se tornar a deusa do Submundo, fazendo com que os fantasmas do submundo a seguissem.</p>

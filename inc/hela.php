        <h2 class="hela">Fraquezas</h2>
        <p class="helatext">Ao usar a vestimenta que lhe serve de manto, capuz e toucado, o corpo de Hela está totalmente vivo e saudável. No entanto, privada de seu manto, Hela volta à sua verdadeira forma. O lado direito de seu corpo está totalmente vivo, mas o lado esquerdo está morto e deteriorado. Diz a lenda que Hela nasceu nesta condição. Foi teorizado que sem seu manto, Hela seria incapaz de deixar o reino dos mortos. Foi demonstrado que sem seu manto, a força vital de Hela é insuficiente para manter sua força física normal. Na verdade, ela é incapaz de ficar de pé ou levitar, ou de projetar raios místicos, e mal consegue engatinhar. Vestir seu manto mais uma vez restaurará Hela à vitalidade total; até mesmo simplesmente tocá-lo permitiria que ela se levantasse novamente.</p>

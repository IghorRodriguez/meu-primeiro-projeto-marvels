        <p class="helatext">A base do mito de Hela se manteve, com exceção do visual meio vivo, meio morto, que foi alterado para a icônica roupa verde e preta, com um capacete marcante com chifres, ou galhos como dizem alguns. Esse visual marcante já aparece no teaser e foi um motivo de grande empolgação pra quem já conhecia a personagem, já que o visual dos personagens de quadrinhos nos cinemas muitas vezes fogem de seu conceito original e icônico.</p>
        <p class="helatext">O mito da deusa do Submundo serviu de base para transformá-la numa vilã recorrente de Thor e Asgard, com a deusa nutrindo ódio por Odin tê-la obrigada a ser a deusa do Hel.</p>
